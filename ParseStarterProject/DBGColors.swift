import UIKit
extension UIColor {
    
    class func danyBaseColor() -> UIColor {
        //return UIColor(red: 0.3, green: 0.4, blue: 0.5, alpha: 1)
        return UIColor(hexString: "FFFFFF")!
    }
    
    class func danyContrastColor() -> UIColor {
        return UIColor(hexString: "#4CD964")!
    }
    
}